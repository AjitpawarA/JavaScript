// Switch Statement in js 

let color = "red";

switch(color){
    case "red":
        console.log("stop the car.....");
        break;
    case "yellow":
        console.log("slow down the car ......");
        break;
    case "green":
        console.log("you can Go now ... ");
        break;
    default:
        console.log("lights are off")
}