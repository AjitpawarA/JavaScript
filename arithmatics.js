

// Arithmatics in js 

let num1 =10;
let num2 =20;

console.log(typeof num1);
console.log(typeof num2)

console.log("num1 + num2 = "+ (num1+num2));
console.log("num1 * num2 = "+ (num1*num2));
console.log("num1 - num2 = "+ (num1-num2));
console.log("num1 / num2 = "+ (num1/num2));
console.log("num1 % num2 = "+ (num1%num2));

// postfix
console.log("after postfix");
console.log(num1++);
console.log("num1++ ="+num1);
console.log(num2--);
console.log("num1-- ="+num2);

// prefix
console.log("after prefix");
console.log(--num1);
console.log("--num1 ="+num1);
console.log(++num2);
console.log("++num2 ="+num2);


